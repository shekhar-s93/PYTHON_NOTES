# Check whether a number is even or odd.

# predefine
a = 9

if(a % 2 == 0):
    print(a, "Even")
else:
    print(a, "Odd")

# user define 

b = int(input("Enter number : "))

if(b % 2 == 0):
    print(b, "Even Number.")
else:
    print(b, "Odd Number.")
