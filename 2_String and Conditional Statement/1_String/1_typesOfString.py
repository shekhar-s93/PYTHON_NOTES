# In python we have 3 types of written types of string.

# 1_ type One

str_1 = 'Shekhar'
print("Type one: ", str_1)

# 2_ type Two

str_2 = "Hello Shekhar"
print("Type two: ", str_2)

# 3_ type Three

str_3 = """Hello Shekhar Suman"""
print("Type three: ", str_3)

# If we want to give new line in string sentences then we have to use (\n), and if we want tabs in string sentences then we have to use (\t).

str = "Hello mr. \nShekhar Suman \t How are you today?"
